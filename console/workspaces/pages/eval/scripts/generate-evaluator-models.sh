#!/bin/bash
# ============================================================================
# Generate Evaluator Editor Models (TypeScript)
# ============================================================================
# Generates evaluator-models.generated.ts from the amp-evaluation library.
# This file provides Monaco completions, data model trees, code templates,
# and hover docs for the evaluator editor UI.
#
# Usage:
#   ./generate-evaluator-models.sh [options]
#
# Options:
#   --amp-eval-version  Version of amp-evaluation to install from PyPI
#   --output            Output file path (default: src/generated/evaluator-models.generated.ts)
#   --dev               Use local source from libs/amp-evaluation
#
# Examples:
#   ./generate-evaluator-models.sh --dev
#   ./generate-evaluator-models.sh --amp-eval-version 0.1.0
# ============================================================================

set -euo pipefail

# Default configuration
AMP_EVAL_VERSION="${AMP_EVAL_VERSION:-}"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
OUTPUT_FILE="${SCRIPT_DIR}/../src/generated/evaluator-models.generated.ts"
DEV_MODE=false

# Parse command line arguments
while [[ $# -gt 0 ]]; do
    case $1 in
        --amp-eval-version)
            AMP_EVAL_VERSION="$2"
            shift 2
            ;;
        --output)
            OUTPUT_FILE="$2"
            shift 2
            ;;
        --dev)
            DEV_MODE=true
            shift
            ;;
        *)
            echo "Unknown option: $1"
            exit 1
            ;;
    esac
done

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
NC='\033[0m'

log_info() { echo -e "${NC}ℹ️  $1${NC}"; }
log_success() { echo -e "${GREEN}✓ $1${NC}"; }
log_error() { echo -e "${RED}✗ $1${NC}"; }

# Check for Python
if ! command -v python3 &> /dev/null; then
    log_error "python3 is required but not installed"
    exit 1
fi

log_info "Generating evaluator editor models..."

# Create output directory if needed
OUTPUT_DIR=$(dirname "${OUTPUT_FILE}")
mkdir -p "${OUTPUT_DIR}"

# Create temporary virtual environment
VENV_DIR=$(mktemp -d)/venv
trap 'deactivate 2>/dev/null || true; rm -rf "$(dirname "${VENV_DIR}")"' EXIT

python3 -m venv "${VENV_DIR}"
source "${VENV_DIR}/bin/activate"

pip install --quiet --upgrade pip

if [[ "${DEV_MODE}" == "true" ]]; then
    LOCAL_AMP_EVAL="${SCRIPT_DIR}/../../../../../libs/amp-evaluation"

    if [[ -d "${LOCAL_AMP_EVAL}" ]]; then
        log_info "Installing amp-evaluation from local source (dev mode)..."
        pip install --quiet -e "${LOCAL_AMP_EVAL}"
    else
        log_error "Local amp-evaluation not found at ${LOCAL_AMP_EVAL}"
        exit 1
    fi
elif [[ -n "${AMP_EVAL_VERSION}" ]]; then
    log_info "Installing amp-evaluation==${AMP_EVAL_VERSION} from PyPI..."
    pip install --quiet "amp-evaluation==${AMP_EVAL_VERSION}"
else
    log_info "Installing latest amp-evaluation from PyPI..."
    pip install --quiet "amp-evaluation"
fi

# Generate the TypeScript source file
OUTPUT_FILE="${OUTPUT_FILE}" python3 << 'PYTHON_SCRIPT' > "${OUTPUT_FILE}"
import json
from amp_evaluation.codegen import get_evaluator_editor_schema

schema = get_evaluator_editor_schema()

# ============================================================================
# TypeScript code generation
# ============================================================================

lines = [
    '// Code generated by scripts/generate-evaluator-models.sh; DO NOT EDIT.',
    '',
    'import type { EvaluatorConfigParam, EvaluatorLevel } from "@agent-management-platform/types";',
    '',
    '// ---------------------------------------------------------------------------',
    '// Types',
    '// ---------------------------------------------------------------------------',
    '',
    'export interface FieldNode {',
    '  name: string;',
    '  type: string;',
    '  description: string;',
    '  children?: FieldNode[];',
    '  isMethod?: boolean;',
    '}',
    '',
    'export interface ModelTree {',
    '  className: string;',
    '  module: string;',
    '  description: string;',
    '  nodes: FieldNode[];',
    '}',
    '',
    'export type CompletionSuggestion = {',
    '  label: string;',
    '  kind: "Class" | "Function" | "Method" | "Property" | "Snippet";',
    '  insertText: string;',
    '  snippet?: boolean;',
    '  detail: string;',
    '  documentation: string;',
    '  sortText?: string;',
    '};',
    '',
    'export type HoverDoc = {',
    '  type: string;',
    '  doc: string;',
    '};',
    '',
]

# ---------------------------------------------------------------------------
# Model trees
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// Model trees — for DataModelReferenceDrawer')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
model_tree_keys = list(schema["model_trees"].keys())
key_union = " | ".join(f'"{k}"' for k in model_tree_keys)
lines.append(f'export type ModelTreeKey = {key_union};')
lines.append('')
lines.append(f'export const MODEL_TREES: Record<ModelTreeKey, ModelTree> = {json.dumps(schema["model_trees"], indent=2)} as const;')
lines.append('')

# ---------------------------------------------------------------------------
# Completions
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// Monaco completions')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')

completions = schema["completions"]
lines.append(f'export const COMMON_COMPLETIONS: CompletionSuggestion[] = {json.dumps(completions["common"], indent=2)};')
lines.append('')
lines.append('export const COMPLETIONS: Record<EvaluatorLevel, CompletionSuggestion[]> = {')
for level in ["trace", "agent", "llm"]:
    lines.append(f'  {level}: {json.dumps(completions[level], indent=2)},')
lines.append('};')
lines.append('')

# ---------------------------------------------------------------------------
# Hover docs
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// Hover documentation')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
lines.append(f'export const HOVER_DOCS: Record<string, HoverDoc> = {json.dumps(schema["hover_docs"], indent=2)};')
lines.append('')

# ---------------------------------------------------------------------------
# Code templates
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// Code templates')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')

def ts_template_string(s):
    """Wrap a string as a TypeScript template literal."""
    # Escape backticks and ${} in the content
    escaped = s.replace('\\', '\\\\').replace('`', '\\`').replace('${', '\\${')
    return f'`{escaped}`'

lines.append('export const CODE_TEMPLATES: Record<EvaluatorLevel, string> = {')
for level in ["trace", "agent", "llm"]:
    lines.append(f'  {level}: {ts_template_string(schema["code_templates"][level])},')
lines.append('};')
lines.append('')

# ---------------------------------------------------------------------------
# LLM Judge templates
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// LLM Judge prompt templates')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
lines.append('export const LLM_JUDGE_TEMPLATES: Record<EvaluatorLevel, string> = {')
for level in ["trace", "agent", "llm"]:
    lines.append(f'  {level}: {ts_template_string(schema["llm_judge_templates"][level])},')
lines.append('};')
lines.append('')

# ---------------------------------------------------------------------------
# LLM Judge variable documentation
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// LLM Judge variable reference (per level)')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
lines.append('export interface LLMJudgeVariable {')
lines.append('  name: string;')
lines.append('  type: string;')
lines.append('  description: string;')
lines.append('  isMethod?: boolean;')
lines.append('}')
lines.append('')
lines.append('export interface LLMJudgeLevelInfo {')
lines.append('  varName: string;')
lines.append('  className: string;')
lines.append('  members: LLMJudgeVariable[];')
lines.append('}')
lines.append('')
lines.append(f'export const LLM_JUDGE_VARIABLES: Record<EvaluatorLevel, LLMJudgeLevelInfo> = {json.dumps(schema["llm_judge_variables"], indent=2)} as const;')
lines.append('')

# ---------------------------------------------------------------------------
# AI Copilot prompts
# ---------------------------------------------------------------------------

lines.append('// ---------------------------------------------------------------------------')
lines.append('// AI Copilot prompt template (single template, resolved at runtime)')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
lines.append(f'export const AI_COPILOT_PROMPT_TEMPLATE: string = {json.dumps(schema["ai_copilot_prompt_template"])};')
lines.append('')
lines.append('')

# ---------------------------------------------------------------------------
# LLM judge base config schema (inherited by all llm_judge evaluators)
# ---------------------------------------------------------------------------

from amp_evaluation.evaluators.base import LLMAsJudgeEvaluator

def is_param_descriptor(obj):
    return (
        not callable(obj)
        and not isinstance(obj, (str, int, float, bool, type, frozenset))
        and hasattr(obj, "to_schema")
        and callable(getattr(obj, "to_schema", None))
        and hasattr(obj, "_attr_name")
    )

llm_base_params = []
for attr_name, attr in vars(LLMAsJudgeEvaluator).items():
    if is_param_descriptor(attr):
        attr._attr_name = attr_name
        llm_base_params.append(attr.to_schema())

lines.append('// ---------------------------------------------------------------------------')
lines.append('// LLM judge base config — inherited by all llm_judge evaluators')
lines.append('// ---------------------------------------------------------------------------')
lines.append('')
lines.append(f'export const LLM_JUDGE_BASE_CONFIG_SCHEMA: EvaluatorConfigParam[] = {json.dumps(llm_base_params, indent=2)};')
lines.append('')

print("\n".join(lines))

# Write the AI copilot guide to the webapp public/prompts directory
import os, sys
output_dir = os.path.dirname(os.path.abspath(os.environ.get("OUTPUT_FILE", __file__)))
# output_dir = console/workspaces/pages/eval/src/generated — go up 5 levels to console/
public_prompts_dir = os.path.normpath(os.path.join(output_dir, "..", "..", "..", "..", "..", "apps", "webapp", "public", "prompts"))
os.makedirs(public_prompts_dir, exist_ok=True)
guide_path = os.path.join(public_prompts_dir, "writing-evaluators.md")
with open(guide_path, "w") as f:
    f.write(schema["ai_copilot_guide"])
print(f"Wrote AI copilot guide to {guide_path}", file=sys.stderr)
PYTHON_SCRIPT

# Append supported packages list (read from evaluation-job/requirements.txt)
REQUIREMENTS_FILE="${SCRIPT_DIR}/../../../../../evaluation-job/requirements.txt"
if [[ -f "$REQUIREMENTS_FILE" ]]; then
    python3 - "$REQUIREMENTS_FILE" >> "${OUTPUT_FILE}" << 'PKGS_SCRIPT'
import re, sys

req_file = sys.argv[1]
with open(req_file) as f:
    lines = f.readlines()

names = []
for line in lines:
    line = line.strip()
    if not line or line.startswith('#'):
        continue
    name = re.split(r'[><=!~;\s]', line)[0].strip()
    # Packages that are installed but intentionally not advertised in the
    # editor's package list.
    if name and name.lower() not in {'litellm'}:
        names.append(name)

# Include WSO2 library first
all_pkgs = ["amp-evaluation"] + names

ts_array = "[\n" + "".join(f'  "{p}",\n' for p in all_pkgs) + "]"
print(f"""
// ---------------------------------------------------------------------------
// Supported packages — pre-installed in the evaluation job image
// ---------------------------------------------------------------------------

export const SUPPORTED_PACKAGES: string[] = {ts_array};
""")
PKGS_SCRIPT
fi

log_success "Generated evaluator editor models to ${OUTPUT_FILE}"
